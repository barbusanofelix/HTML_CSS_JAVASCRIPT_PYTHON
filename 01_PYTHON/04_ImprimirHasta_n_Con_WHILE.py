#Imprimir los numeros hasta n, usando ciclo WHILE
print("Imprime todos los numeros de 1 hasta n")
print()
n=int(input("Ingresa un numero :"))

contador=1  # Defino un contador , inciado en 1

while contador<=n:           # Hacer hasta que contador sea <= a n
    print(contador)          # imprime el contador 
    contador +=1             # incrementamos el contador
    
